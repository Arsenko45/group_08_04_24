# Перевірка паролю
# буде змінна password, яку ми задаємо з консолі, і потім є змінна count(кількість спроб ввести пароль)
# якщо користувач ввів пароль 'qwerty' то тоді вивести на екран шо він ввійшов в систему, якщо ж
# кількість його спроб дорівнює 3, то вивести на екран спробуйте ввести пароль пізніше і зупинити виконання циклу
password = ''
count = 0
while password != 'qwerty':
    password = input('Введіть пароль: ')
    count += 1